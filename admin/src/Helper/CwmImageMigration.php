    /**
     * Determine whether a path points to a core component image
     *
     * Core images are shipped with the component in media/com_proclaim/images/.
     * They must never be migrated, deleted, or regenerated by Image Tools.
     *
     * Handles three path formats:
     *  - Full path:    media/com_proclaim/images/speaker24.png
     *  - Legacy path:  images/biblestudy/speaker24.png  (old BibleStudy location)
     *  - Bare filename: speaker24.png
     *
     * @param   string  $path  Image path (relative to JPATH_ROOT or bare filename)
     *
     * @return  bool
     *
     * @since 10.1.0
     */
    public static function isCoreImage(string $path): bool
    {
        $path = ltrim($path, '/');

        if ($path === '') {
            return false;
        }

        // Direct path to core images directory
        if (str_starts_with($path, self::CORE_IMAGE_DIR . '/')) {
            return true;
        }

        // Legacy path: images/biblestudy/<filename> with no deeper subdirectory
        // (structured user paths look like images/biblestudy/studies/alias-123/file.jpg)
        if (
            str_starts_with($path, 'images/biblestudy/')
            && !str_contains(substr($path, 18), '/')
            && is_file(JPATH_ROOT . '/' . self::CORE_IMAGE_DIR . '/' . basename($path))
        ) {
            return true;
        }

        // Bare filename that exists in the core images directory
        if (
            !str_contains($path, '/')
            && is_file(JPATH_ROOT . '/' . self::CORE_IMAGE_DIR . '/' . $path)
        ) {
            return true;
        }

        return false;
    }

    /**
     * Get records needing migration for a specific type
     *
     * @param   string  $type  Record type: 'studies', 'teachers', or 'series'
     *
     * @return  array  Records with images not in the new folder structure
     *
     * @since 10.2.0
     */
    public static function getRecordsNeedingMigration(string $type): array
    {
        $db    = Factory::getContainer()->get('DatabaseDriver');
        $query = $db->getQuery(true);

        // SQL exclusion for core component images
        $coreExclude = ' NOT LIKE ' . $db->q('media/com_proclaim/images/%');

        switch ($type) {
            case 'studies':
                $col = $db->quoteName('thumbnailm');
                $query->select(
                    $db->quoteName('id') . ', ' . $db->quoteName('studytitle') . ', ' . $db->quoteName('alias') . ', '
                    . $db->quoteName('thumbnailm', 'image_path') . ', ' . $db->quoteName('image', 'original_path')
                )
                    ->from($db->quoteName('#__bsms_studies'))
                    ->where($col . ' IS NOT NULL')
                    ->where($col . ' != ' . $db->q(''))
                    ->where('LENGTH(' . $col . ') > 1')
                    ->where($col . ' NOT LIKE ' . $db->q('images/biblestudy/studies/%-%/%'))
                    ->where($col . $coreExclude);
                break;

            case 'teachers':
                $col = $db->quoteName('teacher_thumbnail');
                $query->select(
                    $db->quoteName('id') . ', ' . $db->quoteName('teachername') . ', ' . $db->quoteName('alias') . ', '
                    . $db->quoteName('teacher_thumbnail', 'image_path') . ', '
                    . 'COALESCE(' . $db->quoteName('teacher_image') . ', ' . $db->quoteName('image') . ') AS '
                    . $db->quoteName('original_path')
                )
                    ->from($db->quoteName('#__bsms_teachers'))
                    ->where($col . ' IS NOT NULL')
                    ->where($col . ' != ' . $db->q(''))
                    ->where('LENGTH(' . $col . ') > 1')
                    ->where($col . ' NOT LIKE ' . $db->q('images/biblestudy/teachers/%-%/%'))
                    ->where($col . $coreExclude);
                break;

            case 'series':
                $col = $db->quoteName('series_thumbnail');
                $query->select(
                    $db->quoteName('id') . ', ' . $db->quoteName('series_text', 'title') . ', ' . $db->quoteName('alias') . ', '
                    . $db->quoteName('series_thumbnail', 'image_path') . ', ' . $db->quoteName('image', 'original_path')
                )
                    ->from($db->quoteName('#__bsms_series'))
                    ->where($col . ' IS NOT NULL')
                    ->where($col . ' != ' . $db->q(''))
                    ->where('LENGTH(' . $col . ') > 1')
                    ->where($col . ' NOT LIKE ' . $db->q('images/biblestudy/series/%-%/%'))
                    ->where($col . $coreExclude);
                break;

            default:
                return [];
        }

        $db->setQuery($query);
        $rows = $db->loadObjectList() ?: [];

        // PHP-level filter for bare filenames and legacy paths that resolve to core images
        return array_values(array_filter(
            $rows,
            fn ($row) => !self::isCoreImage($row->image_path)
        ));
    }

    /**
     * Get migration counts for all types
     *
     * @return  array{studies: int, teachers: int, series: int, total: int}
     *
     * @since 10.2.0
     */
    public static function getMigrationCounts(): array
    {
        $counts = [
            'studies'  => \count(self::getRecordsNeedingMigration('studies')),
            'teachers' => \count(self::getRecordsNeedingMigration('teachers')),
            'series'   => \count(self::getRecordsNeedingMigration('series')),
        ];

        $counts['total'] = $counts['studies'] + $counts['teachers'] + $counts['series'];

        return $counts;
    }

    /**
     * Look up a record from the DB and migrate its images
     *
     * @param   string  $type  Record type: 'studies', 'teachers', or 'series'
     * @param   int     $id    Record ID
     *
     * @return  array{success: bool, newPath: ?string, error: ?string}
     *
     * @since 10.2.0
     */
    public static function migrateRecordById(string $type, int $id): array
    {
        $db    = Factory::getContainer()->get('DatabaseDriver');
        $query = $db->getQuery(true);

        switch ($type) {
            case 'studies':
                $query->select($db->quoteName(['studytitle', 'alias', 'thumbnailm', 'image']))
                    ->from($db->quoteName('#__bsms_studies'))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'teachers':
                $query->select($db->quoteName(['teachername', 'alias', 'teacher_thumbnail', 'teacher_image', 'image']))
                    ->from($db->quoteName('#__bsms_teachers'))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'series':
                $query->select($db->quoteName(['series_text', 'alias', 'series_thumbnail', 'image']))
                    ->from($db->quoteName('#__bsms_series'))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            default:
                return ['success' => false, 'newPath' => null, 'error' => 'Invalid type: ' . $type];
        }

        $db->setQuery($query);
        $row = $db->loadObject();

        if (!$row) {
            return ['success' => false, 'newPath' => null, 'error' => 'Record not found: ' . $type . ' #' . $id];
        }

        // Extract title and image path from the row
        $title   = $row->studytitle ?? $row->teachername ?? $row->series_text ?? '';
        $oldPath = trim($row->thumbnailm ?? $row->teacher_thumbnail ?? $row->series_thumbnail ?? '');

        // Check if the alias-ID folder already has migrated files (previous run
        // may have copied files but failed to update DB columns)
        $existing = self::findExistingMigratedFiles($type, $id, $title, $row->alias ?? '');

        if ($existing !== null) {
            return self::updateDbFromExistingFiles($type, $id, $existing);
        }

        // Prefer the image column (original full-size) over thumbnail when available
        $imagePath = trim($row->image ?? $row->teacher_image ?? '');

        if ($imagePath !== '' && \strlen($imagePath) > 1 && !self::isCoreImage($imagePath)) {
            $imageAbsPath = Path::clean(JPATH_ROOT . '/' . $imagePath);

            if (is_file($imageAbsPath)) {
                $oldPath = $imagePath;
            }
        }

        // Treat '0', single-char junk, and empty as "no image" — skip (not migratable)
        if ($oldPath === '' || \strlen($oldPath) <= 1) {
            return ['success' => false, 'newPath' => null, 'error' => 'No valid image path for ' . $type . ' #' . $id, 'junk' => true];
        }

        // Skip core component images — they live in media/com_proclaim/images/
        if (self::isCoreImage($oldPath)) {
            return ['success' => false, 'newPath' => null, 'error' => 'Core image (skipped): ' . $oldPath, 'core' => true];
        }

        return self::migrateRecord($type, $id, $title, $oldPath);
    }

    /**
     * Migrate a single record's images to the new folder structure
     *
     * @param   string  $type       Record type: 'studies', 'teachers', or 'series'
     * @param   int     $id         Record ID
     * @param   string  $title      Record title for folder naming
     * @param   string  $oldPath    Current image path
     * @param   int     $thumbSize  Thumbnail size (default: 300)
     *
     * @return  array{success: bool, newPath: ?string, error: ?string}
     *
     * @since 10.2.0
     */
    public static function migrateRecord(
        string $type,
        int $id,
        string $title,
        string $oldPath,
        int $thumbSize = 300
    ): array {
        // Build the new folder path with title-ID format
        $alias     = ApplicationHelper::stringURLSafe($title ?: $type);
        $newFolder = 'images/biblestudy/' . $type . '/' . $alias . '-' . $id;

        // Clean Joomla media field metadata (#joomlaImage://...)
        $cleanImage = HTMLHelper::_('cleanImageURL', $oldPath);
        $cleanPath  = $cleanImage->url;

        // Handle bare filenames (no directory separator) by prepending configured image folder
        if (!str_contains($cleanPath, '/')) {
            $folderKeys = [
                'studies'  => 'image_folder',
                'teachers' => 'teacher_image_folder',
                'series'   => 'series_image_folder',
            ];
            $folderKey   = $folderKeys[$type] ?? 'image_folder';
            $imageFolder = Cwmparams::getAdmin()->params->get($folderKey, 'images');
            $cleanPath   = $imageFolder . '/' . $cleanPath;
        }

        // Find the original image (not the thumbnail)
        $oldAbsPath = Path::clean(JPATH_ROOT . '/' . $cleanPath);

        // If this is a thumbnail path, try to find the original full-size image first
        $sourceImage = $cleanPath;
        $sourceFound = true;
        $relocated   = false;
        $foundAt     = null;
        $basename    = basename($cleanPath);

        if (str_contains($basename, 'thumb_')) {
            // Try the exact extension first, then alternate extensions
            $strippedBase  = str_replace('thumb_', '', $basename);
            $strippedName  = pathinfo($strippedBase, PATHINFO_FILENAME);
            $dirName       = \dirname($cleanPath);
            $foundOriginal = false;

            // Try original name as-is, then alternate extensions (thumb may be .jpg but original .png)
            foreach ([$strippedBase, ...array_map(
                fn ($ext) => $strippedName . '.' . $ext,
                ['jpg', 'jpeg', 'png', 'webp', 'gif']
            )] as $candidateName) {
                $possibleOriginal = $dirName . '/' . $candidateName;
                $possibleAbsPath  = Path::clean(JPATH_ROOT . '/' . $possibleOriginal);

                if (is_file($possibleAbsPath)) {
                    $sourceImage   = $possibleOriginal;
                    $foundOriginal = true;
                    break;
                }
            }

            if (!$foundOriginal) {
                if (is_file($oldAbsPath)) {
                    // Thumbnail exists, use it as source
                    $sourceImage = $cleanPath;
                } else {
                    $sourceFound = false;
                }
            }
        } elseif (!is_file($oldAbsPath)) {
            $sourceFound = false;
        }

        // Fallback: search the images directory tree for a matching filename
        if (!$sourceFound) {
            $searchName = str_contains($basename, 'thumb_')
                ? str_replace('thumb_', '', $basename)
                : $basename;

            $found = self::findImageFile($searchName, $type);

            // If original not found but this is a thumb_ path, also try the thumbnail filename
            if ($found === null && str_contains($basename, 'thumb_')) {
                $found = self::findImageFile($basename, $type);
            }

            if ($found !== null) {
                $sourceImage = $found;
                $sourceFound = true;
                $relocated   = true;
                $foundAt     = $found;
            }
        }

        // If source file is still missing, report it but do NOT clear the DB field.
        // The admin can clear unresolvable records manually via the dedicated button.
        if (!$sourceFound) {
            return [
                'success'      => false,
                'newPath'      => null,
                'error'        => 'Source file not found',
                'missingPath'  => $cleanPath,
                'absolutePath' => $oldAbsPath,
            ];
        }

        // Create new thumbnail using Cwmthumbnail
        try {
            $result = Cwmthumbnail::create($sourceImage, $newFolder, $thumbSize, $title);
        } catch (\Throwable $e) {
            return [
                'success'     => false,
                'newPath'     => null,
                'error'       => 'Thumbnail error: ' . $e->getMessage(),
                'missingPath' => $cleanPath,
            ];
        }

        if ($result === false) {
            return [
                'success'     => false,
                'newPath'     => null,
                'error'       => 'Failed to create thumbnail (file copy or GD init failed)',
                'missingPath' => $cleanPath,
            ];
        }

        // Update database
        $db    = Factory::getContainer()->get('DatabaseDriver');
        $query = $db->getQuery(true);

        switch ($type) {
            case 'studies':
                $query->update($db->quoteName('#__bsms_studies'))
                    ->set($db->quoteName('thumbnailm') . ' = ' . $db->q($result['thumbnail']))
                    ->set($db->quoteName('image') . ' = ' . $db->q($result['image']))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'teachers':
                $query->update($db->quoteName('#__bsms_teachers'))
                    ->set($db->quoteName('teacher_thumbnail') . ' = ' . $db->q($result['thumbnail']))
                    ->set($db->quoteName('teacher_image') . ' = ' . $db->q($result['image']))
                    ->set($db->quoteName('image') . ' = ' . $db->q($result['image']))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'series':
                $query->update($db->quoteName('#__bsms_series'))
                    ->set($db->quoteName('series_thumbnail') . ' = ' . $db->q($result['thumbnail']))
                    ->set($db->quoteName('image') . ' = ' . $db->q($result['image']))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;
        }

        $db->setQuery($query);
        $db->execute();

        $response = ['success' => true, 'newPath' => $result['thumbnail'], 'error' => null];

        if ($relocated) {
            $response['relocated']    = true;
            $response['originalPath'] = $cleanPath;
            $response['foundAt']      = $foundAt;
        }

        return $response;
    }

    /**
     * Check if an alias-ID folder already contains migrated files for a record
     *
     * A previous migration run may have copied files to the proper folder but
     * failed to update the DB columns. This detects that case so we can fix
     * the DB without re-copying files.
     *
     * @param   string  $type   Record type: 'studies', 'teachers', or 'series'
     * @param   int     $id     Record ID
     * @param   string  $title  Record title (used to build folder name)
     * @param   string  $alias  Record alias (preferred over title for folder name)
     *
     * @return  ?array{image: string, thumbnail: string}  Relative paths if found, null otherwise
     *
     * @since 10.1.0
     */
    private static function findExistingMigratedFiles(string $type, int $id, string $title, string $alias): ?array
    {
        // Build the expected alias-ID folder name
        $folderAlias = $alias ?: ApplicationHelper::stringURLSafe($title ?: $type);
        $expectedDir = JPATH_ROOT . '/images/biblestudy/' . $type . '/' . $folderAlias . '-' . $id;

        // Also check bare-ID folder (legacy migration artefact: folder named just "528" instead of "alias-528")
        $bareIdDir = JPATH_ROOT . '/images/biblestudy/' . $type . '/' . $id;

        $dirsToCheck = [];

        if (is_dir($expectedDir)) {
            $dirsToCheck[] = ['dir' => $expectedDir, 'relDir' => 'images/biblestudy/' . $type . '/' . $folderAlias . '-' . $id];
        }

        if (is_dir($bareIdDir)) {
            $dirsToCheck[] = ['dir' => $bareIdDir, 'relDir' => 'images/biblestudy/' . $type . '/' . $id];
        }

        if (empty($dirsToCheck)) {
            return null;
        }

        $imageExts = ['jpg', 'jpeg', 'png', 'webp', 'gif'];

        foreach ($dirsToCheck as $candidate) {
            $thumbFile = null;
            $imageFile = null;

            $files = @scandir($candidate['dir']);

            if ($files === false) {
                continue;
            }

            foreach ($files as $file) {
                if ($file === '.' || $file === '..') {
                    continue;
                }

                $ext = strtolower(pathinfo($file, PATHINFO_EXTENSION));

                if (!\in_array($ext, $imageExts, true)) {
                    continue;
                }

                if (str_starts_with($file, 'thumb_') && $ext !== 'webp') {
                    $thumbFile = $file;
                } elseif (!str_starts_with($file, 'thumb_') && $ext !== 'webp') {
                    $imageFile = $file;
                }
            }

            if ($thumbFile !== null || $imageFile !== null) {
                return [
                    'image'     => $imageFile ? $candidate['relDir'] . '/' . $imageFile : '',
                    'thumbnail' => $thumbFile ? $candidate['relDir'] . '/' . $thumbFile : '',
                ];
            }
        }

        return null;
    }

    /**
     * Update DB columns from already-migrated files in the alias-ID folder
     *
     * @param   string  $type      Record type: 'studies', 'teachers', or 'series'
     * @param   int     $id        Record ID
     * @param   array   $existing  Array with 'image' and 'thumbnail' relative paths
     *
     * @return  array{success: bool, newPath: ?string, error: ?string}
     *
     * @since 10.1.0
     */
    private static function updateDbFromExistingFiles(string $type, int $id, array $existing): array
    {
        $db    = Factory::getContainer()->get('DatabaseDriver');
        $query = $db->getQuery(true);

        $thumbPath = $existing['thumbnail'];
        $imagePath = $existing['image'];

        switch ($type) {
            case 'studies':
                $query->update($db->quoteName('#__bsms_studies'))
                    ->set($db->quoteName('thumbnailm') . ' = ' . $db->q($thumbPath))
                    ->set($db->quoteName('image') . ' = ' . $db->q($imagePath))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'teachers':
                $query->update($db->quoteName('#__bsms_teachers'))
                    ->set($db->quoteName('teacher_thumbnail') . ' = ' . $db->q($thumbPath))
                    ->set($db->quoteName('teacher_image') . ' = ' . $db->q($imagePath))
                    ->set($db->quoteName('image') . ' = ' . $db->q($imagePath))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'series':
                $query->update($db->quoteName('#__bsms_series'))
                    ->set($db->quoteName('series_thumbnail') . ' = ' . $db->q($thumbPath))
                    ->set($db->quoteName('image') . ' = ' . $db->q($imagePath))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;
        }

        $db->setQuery($query);
        $db->execute();

        return [
            'success'  => true,
            'newPath'  => $thumbPath,
            'error'    => null,
            'relinked' => true,
        ];
    }

    /**
     * Get a batch of records to migrate
     *
     * @param   string  $type        Record type
     * @param   int     $limit       Maximum number of records to return
     * @param   int[]   $excludeIds  Record IDs to skip (already failed)
     *
     * @return  array{records: array, remaining: int}
     *
     * @since 10.2.0
     */
    public static function getBatch(string $type, int $limit = 10, array $excludeIds = []): array
    {
        $records = self::getRecordsNeedingMigration($type);

        // Filter out records that already failed — they stay in the DB
        // but the JS tracks them and sends their IDs to skip
        if (!empty($excludeIds)) {
            $records = array_values(array_filter(
                $records,
                fn ($r) => !\in_array((int) $r->id, $excludeIds, true)
            ));
        }

        $batch     = \array_slice($records, 0, $limit);
        $remaining = \count($records) - \count($batch);

        return [
            'records'   => $batch,
            'remaining' => $remaining,
        ];
    }

    /**
     * Clear the image field for a record so it won't be returned by getRecordsNeedingMigration()
     *
     * Called when the source file is missing — the image is gone, nothing to migrate.
     * Logs the cleared value to a CSV file so admins can search for the file manually.
     *
     * @param   string  $type     Record type: 'studies', 'teachers', or 'series'
     * @param   int     $id       Record ID
     * @param   string  $oldPath  The image path being cleared (for the log)
     * @param   string  $title    Record title (for the log)
     *
     * @return  void
     *
     * @since 10.2.0
     */
    private static function clearImageField(string $type, int $id, string $oldPath = '', string $title = ''): void
    {
        // Log the cleared value before wiping it
        self::logClearedImage($type, $id, $oldPath, $title);

        $db    = Factory::getContainer()->get('DatabaseDriver');
        $query = $db->getQuery(true);

        switch ($type) {
            case 'studies':
                $query->update($db->quoteName('#__bsms_studies'))
                    ->set($db->quoteName('thumbnailm') . ' = ' . $db->q(''))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'teachers':
                $query->update($db->quoteName('#__bsms_teachers'))
                    ->set($db->quoteName('teacher_thumbnail') . ' = ' . $db->q(''))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;

            case 'series':
                $query->update($db->quoteName('#__bsms_series'))
                    ->set($db->quoteName('series_thumbnail') . ' = ' . $db->q(''))
                    ->set($db->quoteName('image') . ' = ' . $db->q(''))
                    ->where($db->quoteName('id') . ' = ' . (int) $id);
                break;
        }

        $db->setQuery($query);
        $db->execute();
    }

    /**
     * Log a cleared image value to CSV for administrator review
     *
     * Writes to administrator/logs/proclaim_cleared_images.csv so admins
     * can search other locations for the missing files.
     *
     * @param   string  $type     Record type
     * @param   int     $id       Record ID
     * @param   string  $oldPath  The image path that was cleared
